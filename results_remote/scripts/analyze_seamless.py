"""
[RQ1-CMDP] SEAMLESS deployment analysis -- NUMBERS ONLY (no conclusions; the operator
cross-checks every value against the raw .mat).

Deploy_seamless_800ep: train 600 ep (== canonical) then continue the SAME env FROZEN for 200
ep at the certified sigma=0.3 (AoI NOT reset, vehicles keep driving). The decisive comparison
is per seed: training worst-platoon violation (last-100-ep, on the certified geometry) vs the
frozen seamless-tail worst-platoon (same continued geometry). If they stay close, the
worst-platoon guarantee survives frozen deployment on the certified geometry; the earlier
~0.35 was largely the geometry-restart + AoI-reset artifact of the --eval_only protocol.

  training worst = max_j (AoI_evolution.mat > tau).mean(axis=(1,2))          [last 100 train ep]
  tail worst     = max_j viol_rate_seamless.mat ;  tail net-mean = mean_j(...)
Acceptance gate: seamless run's first-600 AoI_evolution.mat must match Canonical_ep600's
(allclose) -> confirms the tail/save code did not perturb training.
Writes results_remote/RQ1_DEPLOY_SEAMLESS.md. Exit 2 if any of the 12 runs is missing.
"""
import os
import sys
import numpy as np
import scipy.io

RR = os.path.dirname(os.path.dirname(os.path.realpath(__file__)))   # results_remote/
MODEL = os.path.join(RR, "..", "1-ModifiedMADDPGwithTDec", "model")
SEAMLESS = os.path.join(MODEL, "Deploy_seamless_800ep")
REPORT = os.path.join(RR, "RQ1_DEPLOY_SEAMLESS.md")
SEEDS = [2, 3, 4, 5, 6, 7]
TAU = 8.0
ARMS = [("soft", "soft", "base_seamless800", "base_ep600"),
        ("pid",  "hard", "t8e10_pid_seamless800", "t8e10_pid_ep600")]

# location-agnostic resolver: find a run dir by NAME anywhere under model/ (canonical lives in
# Canonical_ep600/, the seamless runs in Deploy_seamless_800ep/).
_RIDX = {}
for _dp, _dns, _fn in os.walk(MODEL):
    for _dn in _dns:
        if _dn.startswith("marl_model_"):
            _RIDX.setdefault(_dn, os.path.join(_dp, _dn))
def R(name):
    return _RIDX.get(name, os.path.join(SEAMLESS, name))


def load(run, fn, key):
    p = os.path.join(R(run), fn)
    return None if not os.path.exists(p) else np.asarray(scipy.io.loadmat(p)[key], np.float64)


def train_worst_vec(run):
    ev = load(run, "AoI_evolution.mat", "AoI_evolution")
    return None if ev is None else (ev > TAU).mean(axis=(1, 2))   # (5,)


def main():
    missing = []
    for (_, mode, tag, _) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            if load(run, "viol_rate_seamless.mat", "viol_rate_seamless") is None:
                missing.append(run)
    if missing:
        sys.stderr.write("[seamless] missing %d runs (e.g. %s) -- not writing report\n"
                         % (len(missing), missing[:4]))
        sys.exit(2)

    L = []; w = L.append
    w("# RQ1 — SEAMLESS frozen deployment (train 600 + frozen tail 200 on the SAME env)\n")
    w("**Auto-generated by `scripts/deploy_seamless_driver.ps1` -> `scripts/analyze_seamless.py`** "
      "(detached). NUMBERS ONLY; no conclusions drawn. The operator cross-checks the raw `.mat`. "
      "Tail = 200 frozen episodes at the certified sigma=0.3, AoI NOT reset, env continued from "
      "the end of training (renew_positions every 20 ep). worst = `viol_rate_seamless.max()`.\n")

    # ---- per-seed: training worst vs seamless-tail worst ----
    w("## 1. Per seed: training worst-platoon vs frozen seamless-tail worst-platoon\n")
    w("| arm | seed | train worst | tail worst | tail net-mean | gap(tail-train) | canon AoI match |")
    w("|---|---|---|---|---|---|---|")
    pooled = {"soft": [], "pid": []}
    pooled_train = {"soft": [], "pid": []}
    canon_all_ok = True
    for (arm, mode, tag, ctag) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            canon = "marl_model_%s_seed%d_%s" % (mode, s, ctag)
            tw = train_worst_vec(run); tr_worst = float(tw.max()) if tw is not None else float("nan")
            tail = load(run, "viol_rate_seamless.mat", "viol_rate_seamless").ravel()
            t_worst = float(tail.max()); t_mean = float(tail.mean())
            pooled[arm].append(t_worst); pooled_train[arm].append(tr_worst)
            ev_s = load(run, "AoI_evolution.mat", "AoI_evolution")
            ev_c = load(canon, "AoI_evolution.mat", "AoI_evolution")
            if ev_c is None:
                match = "n/a"
            else:
                ok = bool(np.allclose(ev_s, ev_c, atol=1e-4, rtol=0))
                match = "YES" if ok else "NO(max|d|=%.3g)" % float(np.abs(ev_s - ev_c).max())
                canon_all_ok = canon_all_ok and ok
            w("| %s | %d | %.3f | %.3f | %.3f | %+.3f | %s |"
              % (arm, s, tr_worst, t_worst, t_mean, t_worst - tr_worst, match))
    w("")

    # ---- pooled ----
    w("## 2. Pooled worst-platoon violation (over seeds 2-7)\n")
    w("| arm | training (last-100ep) | seamless tail (frozen, sigma=0.3) |")
    w("|---|---|---|")
    for arm in ("soft", "pid"):
        tr = np.array(pooled_train[arm]); tl = np.array(pooled[arm])
        w("| %s | %.3f+-%.3f | %.3f+-%.3f |"
          % (arm, tr.mean(), tr.std(ddof=1), tl.mean(), tl.std(ddof=1)))
    w("\n(reference: the --eval_only restart-geometry warm eval gave pid tail-worst ~0.35; "
      "training pid worst ~0.13.)\n")

    # ---- sanity ----
    w("## 3. Sanity checks\n")
    bad5 = []; nopkl = []
    for (_, mode, tag, _) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            v = load(run, "viol_rate_seamless.mat", "viol_rate_seamless")
            if v is None or v.reshape(-1).shape[0] != 5:
                bad5.append(run)
            if not os.path.exists(os.path.join(R(run), "Scenario_Reconstruct.pkl")):
                nopkl.append(run)
    w("**(a) every viol_rate_seamless.mat has 5 entries:** %s\n"
      % ("ALL PASS" if not bad5 else "FAIL: " + "; ".join(bad5)))
    w("**(b) Scenario_Reconstruct.pkl present in every run:** %s\n"
      % ("ALL PASS" if not nopkl else "MISSING: " + "; ".join(nopkl)))
    w("**(c) ACCEPTANCE GATE — first-600-ep AoI_evolution matches Canonical_ep600:** %s\n"
      % ("ALL PASS" if canon_all_ok else "FAIL (see canon-match column above)"))

    w("## 4. Reproduce\n```\npython results_remote/scripts/analyze_seamless.py\n```")
    with open(REPORT, "w", encoding="utf-8") as f:
        f.write("\n".join(L) + "\n")
    print("[seamless] wrote", REPORT, "| canon-gate:", "PASS" if canon_all_ok else "FAIL")


if __name__ == "__main__":
    main()
