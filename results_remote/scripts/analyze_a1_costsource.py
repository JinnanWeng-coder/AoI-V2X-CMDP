"""
[RQ1-CMDP] Ablation A1 — cost-critic necessity (raw/RCPO vs learned Q^c) — NUMBERS ONLY.

CRITIC arm (ours)  = Canonical_ep600 hard PID (marl_model_hard_seed*_t8e10_pid_ep600), which the
                     current code reproduces byte-identically under --cost_source critic (default).
RAW arm (RCPO)     = Ablations_ep600/cost_source/marl_model_hard_seed*_t8e10_pid_ep600_rawcost
                     (--cost_source raw: -lambda*cost folded into the task-2 reward, no separate Q^c).

Canonical metric: worst-platoon viol = (AoI_evolution>tau).mean(axis=(1,2)).max(). Also report the
late-std (per-episode viol oscillation of the most-oscillating platoon) and mean power. Defends (c)
if RAW is worse / noisier; demote (c) if RAW == CRITIC. Writes results_remote/RQ1_ABLATION_COSTSOURCE.md.
Exit 2 if any of the 6 raw runs is missing.
"""
import os
import sys
import numpy as np
import scipy.io

RR = os.path.dirname(os.path.dirname(os.path.realpath(__file__)))
MODEL = os.path.join(RR, "..", "1-ModifiedMADDPGwithTDec", "model")
REPORT = os.path.join(RR, "RQ1_ABLATION_COSTSOURCE.md")
SEEDS = [2, 3, 4, 5, 6, 7]
TAU = 8.0

RIDX = {}
for dp, dns, fn in os.walk(MODEL):
    for dn in dns:
        if dn.startswith("marl_model_"):
            RIDX.setdefault(dn, os.path.join(dp, dn))
def R(run): return RIDX.get(run, os.path.join(MODEL, run))
def has(run): return run in RIDX
def L(run, fn, k):
    p = os.path.join(R(run), fn)
    return None if not os.path.exists(p) else np.asarray(scipy.io.loadmat(p)[k], np.float64)

def critic_run(s): return "marl_model_hard_seed%d_t8e10_pid_ep600" % s
def raw_run(s):    return "marl_model_hard_seed%d_t8e10_pid_ep600_rawcost" % s

def worst(run):
    return float((L(run, "AoI_evolution.mat", "AoI_evolution") > TAU).mean(axis=(1, 2)).max())
def netmean(run):
    return float((L(run, "AoI_evolution.mat", "AoI_evolution") > TAU).mean(axis=(1, 2)).mean())
def osc_std(run):
    vr = L(run, "viol_rate.mat", "viol_rate")[:, -100:]
    return float(vr.std(axis=1).max())
def power(run):
    p = L(run, "power.mat", "power")
    return float(p.mean()) if p is not None else float("nan")


def main():
    missing = [raw_run(s) for s in SEEDS if not has(raw_run(s))]
    if missing:
        sys.stderr.write("[a1] missing %d raw runs (e.g. %s)\n" % (len(missing), missing[:3]))
        sys.exit(2)

    L_ = []; w = L_.append
    w("# RQ1 — Ablation A1: cost-critic necessity (raw/RCPO vs learned Q^c)\n")
    w("**Auto-generated by `a1_costsource_driver.ps1` -> `analyze_a1_costsource.py`** (detached). "
      "NUMBERS ONLY; operator cross-checks raw `.mat`. CRITIC = ours (Canonical_ep600 PID, == "
      "`--cost_source critic`); RAW = RCPO-style (`--cost_source raw`). Canonical metric: "
      "worst-platoon `(AoI_evolution>tau).mean(axis=(1,2)).max()`.\n")

    w("## 1. Per seed: worst-platoon viol / net-mean / osc-late-std / power\n")
    w("| seed | CRITIC worst | RAW worst | CRITIC net | RAW net | CRITIC osc-std | RAW osc-std | CRITIC pow | RAW pow |")
    w("|---|---|---|---|---|---|---|---|---|")
    cw = []; rw = []; cs = []; rs = []; cp = []; rp = []
    for s in SEEDS:
        c, r = critic_run(s), raw_run(s)
        cw.append(worst(c)); rw.append(worst(r)); cs.append(osc_std(c)); rs.append(osc_std(r))
        cp.append(power(c)); rp.append(power(r))
        w("| %d | %.3f | %.3f | %.3f | %.3f | %.3f | %.3f | %.2f | %.2f |"
          % (s, worst(c), worst(r), netmean(c), netmean(r), osc_std(c), osc_std(r), power(c), power(r)))
    cw = np.array(cw); rw = np.array(rw); cs = np.array(cs); rs = np.array(rs); cp = np.array(cp); rp = np.array(rp)

    w("\n## 2. Pooled (seeds 2-7, mean +- sample-SD)\n")
    w("| metric | CRITIC (ours) | RAW (RCPO) | RAW - CRITIC |")
    w("|---|---|---|---|")
    def row(nm, a, b):
        w("| %s | %.3f+-%.3f | %.3f+-%.3f | %+.3f |" % (nm, a.mean(), a.std(ddof=1), b.mean(), b.std(ddof=1), b.mean() - a.mean()))
    row("worst-platoon viol", cw, rw)
    row("worst-platoon osc-std", cs, rs)
    row("mean power (dBm)", cp, rp)
    w("\n- worst-CASE seed: CRITIC %.3f vs RAW %.3f" % (cw.max(), rw.max()))
    w("- RAW worse (higher worst) on %d/6 seeds; RAW noisier (higher osc-std) on %d/6 seeds.\n"
      % (int(np.sum(rw > cw + 1e-9)), int(np.sum(rs > cs + 1e-9))))
    w("(Reading: RAW worse/noisier -> the separate learned cost critic Q^c is necessary, keep (c) "
      "in the method figure. RAW == CRITIC -> demote (c) to an inherited implementation detail.)\n")

    w("## 3. Reproduce\n```\npython results_remote/scripts/analyze_a1_costsource.py\n```")
    with open(REPORT, "w", encoding="utf-8") as f:
        f.write("\n".join(L_) + "\n")
    print("[a1] wrote", REPORT)
    print("[a1] pooled worst: CRITIC %.3f vs RAW %.3f ; osc-std CRITIC %.3f vs RAW %.3f"
          % (cw.mean(), rw.mean(), cs.mean(), rs.mean()))


if __name__ == "__main__":
    main()
