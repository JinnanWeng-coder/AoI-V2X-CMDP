"""
[RQ1-CMDP] Ablation A2 — epsilon-sensitivity sweep ("knob -> level"). NUMBERS ONLY;
operator cross-checks raw .mat + makes the figure locally.

Arms (PID, tau=8, ep600, seeds 2-7), one variable = eps:
  eps=0.05  marl_model_hard_seed{S}_t8e5_pid_ep600    (Ablations_ep600/eps_sweep/, NEW)
  eps=0.10  marl_model_hard_seed{S}_t8e10_pid_ep600   (Canonical_ep600/, REUSED)
  eps=0.20  marl_model_hard_seed{S}_t8e20_pid_ep600    (Ablations_ep600/eps_sweep/, NEW)
Runs resolved by NAME anywhere under model/ (subfolder is organizational only).

CLAIM: the achieved worst-platoon violation TRACKS eps (monotone up with eps) and lambda_j
RELAXES as eps loosens -> the constraint is a tunable target (a knob -> level), not a fixed
penalty. This is the POSITIVE counterpart to ablation #4 (fixed-weight, the negative control).

Metrics (canonical audit, tau=8):
  worst   = (AoI_evolution>8).mean(axis=(1,2)).max()      # worst-served platoon
  netmean = (AoI_evolution>8).mean(axis=(1,2)).mean()     # network mean (context)
  lam*    = lambda.mat[:,-100:].mean(axis=1) -> bottleneck = max over platoons, mean = mean

HONEST framing: do NOT expect worst == eps exactly (chance-constraint + RL variance +
two-timescale dual; tighter eps is harder to hit). Report MONOTONE tracking + lambda relaxation.
Writes results_remote/RQ1_ABLATION_EPS_SWEEP.md. Exit 2 if any of the 12 NEW runs is missing.
"""
import os
import sys
import numpy as np
import scipy.io

RR = os.path.dirname(os.path.dirname(os.path.realpath(__file__)))
MODEL = os.path.join(RR, "..", "1-ModifiedMADDPGwithTDec", "model")
REPORT = os.path.join(RR, "RQ1_ABLATION_EPS_SWEEP.md")
SEEDS = [2, 3, 4, 5, 6, 7]
TAU = 8.0
# (eps, tag, is_new)
ARMS = [(0.05, "t8e5_pid_ep600", True),
        (0.10, "t8e10_pid_ep600", False),
        (0.20, "t8e20_pid_ep600", True)]

RIDX = {}
for dp, dns, fn in os.walk(MODEL):
    for dn in dns:
        if dn.startswith("marl_model_"):
            RIDX.setdefault(dn, os.path.join(dp, dn))
def has(run): return run in RIDX
def L(run, fn, k):
    p = os.path.join(RIDX[run], fn)
    return None if not os.path.exists(p) else np.asarray(scipy.io.loadmat(p)[k], np.float64)

def run(s, tag): return "marl_model_hard_seed%d_%s" % (s, tag)
def worst(r):   return float((L(r, "AoI_evolution.mat", "AoI_evolution") > TAU).mean(axis=(1, 2)).max())
def netmean(r): return float((L(r, "AoI_evolution.mat", "AoI_evolution") > TAU).mean(axis=(1, 2)).mean())
def lam(r):
    lm = L(r, "lambda.mat", "lambda")[:, -100:].mean(axis=1)
    return float(lm.max()), float(lm.mean())


def main():
    missing = [run(s, tag) for (eps, tag, new) in ARMS if new for s in SEEDS if not has(run(s, tag))]
    if missing:
        sys.stderr.write("[a2] missing %d new runs (e.g. %s)\n" % (len(missing), missing[:3]))
        sys.exit(2)

    # gather: per arm, per seed
    epslist = [e for (e, _, _) in ARMS]
    W_ = {}; NM = {}; LB = {}; LM = {}
    for (eps, tag, _) in ARMS:
        W_[eps] = np.array([worst(run(s, tag)) for s in SEEDS])
        NM[eps] = np.array([netmean(run(s, tag)) for s in SEEDS])
        lb = []; lm = []
        for s in SEEDS:
            b, m = lam(run(s, tag)); lb.append(b); lm.append(m)
        LB[eps] = np.array(lb); LM[eps] = np.array(lm)

    O = []; w = O.append
    w("# RQ1 — Ablation A2: epsilon-sensitivity sweep (knob -> level)\n")
    w("**Auto-generated by `a2_eps_sweep_driver.ps1` -> `analyze_a2_eps.py`** (detached). NUMBERS "
      "ONLY; operator cross-checks raw `.mat`. PID dual, tau=8, ep600, seeds 2-7; ONE variable = "
      "eps. eps=0.10 is the canonical PID arm (reused); eps=0.05/0.20 are `Ablations_ep600/eps_sweep/`. "
      "Worst-platoon viol = `(AoI_evolution>8).mean(axis=(1,2)).max()`; lambda = `lambda.mat[:,-100:]"
      ".mean(axis=1)` (bottleneck = max over platoons).\n")
    w("**Claim:** the achieved worst-platoon violation TRACKS eps and lambda RELAXES as eps loosens "
      "-> the constraint is a tunable target, not a fixed penalty (positive counterpart to ablation "
      "#4). NOTE: worst need not equal eps exactly (chance-constraint + RL variance + dual); the "
      "evidence is MONOTONE tracking + lambda relaxation.\n")

    # 1. tracking grid: worst-platoon per seed x eps
    w("## 1. Worst-platoon violation per seed, across eps (the tracking quantity)\n")
    w("| seed | eps=0.05 | eps=0.10 | eps=0.20 | monotone up? |")
    w("|---|---|---|---|---|")
    mono_w = 0
    for k, s in enumerate(SEEDS):
        a, b, c = W_[0.05][k], W_[0.10][k], W_[0.20][k]
        up = (a <= b + 1e-9) and (b <= c + 1e-9)
        mono_w += int(up)
        w("| %d | %.3f | %.3f | %.3f | %s |" % (s, a, b, c, "yes" if up else "no"))
    w("| **pooled mean** | **%.3f** | **%.3f** | **%.3f** | %d/6 seeds |"
      % (W_[0.05].mean(), W_[0.10].mean(), W_[0.20].mean(), mono_w))

    # 2. pooled summary incl. lambda relaxation
    w("\n## 2. Pooled per eps: worst / net-mean / bottleneck-lambda / mean-lambda (mean +- SD, n=6)\n")
    w("| eps | worst-platoon viol | net-mean viol | bottleneck lambda | mean lambda |")
    w("|---|---|---|---|---|")
    for eps in epslist:
        w("| %.2f | %.3f +- %.3f | %.3f | %.2f +- %.2f | %.2f |"
          % (eps, W_[eps].mean(), W_[eps].std(ddof=1), NM[eps].mean(),
             LB[eps].mean(), LB[eps].std(ddof=1), LM[eps].mean()))

    # 3. tracking diagnostics
    w("\n## 3. Tracking diagnostics\n")
    # Spearman(eps, worst) over all 18 (seed,eps) points
    try:
        from scipy.stats import spearmanr
        xs = np.repeat(epslist, len(SEEDS))
        ys = np.concatenate([W_[e] for e in epslist])
        rho, prho = spearmanr(xs, ys)
    except Exception:
        rho, prho = float("nan"), float("nan")
    mono_lam = sum(int(LB[0.05][k] >= LB[0.10][k] - 1e-9 and LB[0.10][k] >= LB[0.20][k] - 1e-9)
                   for k in range(len(SEEDS)))
    w("- worst-platoon vs eps: pooled **%.3f -> %.3f -> %.3f** for eps 0.05/0.10/0.20; "
      "monotone non-decreasing on **%d/6** seeds; Spearman(eps, worst) rho=**%.3f** (p=%.4f, n=18).\n"
      % (W_[0.05].mean(), W_[0.10].mean(), W_[0.20].mean(), mono_w, rho, prho))
    w("- bottleneck lambda vs eps: pooled **%.2f -> %.2f -> %.2f**; monotone non-increasing "
      "(relaxes as eps loosens) on **%d/6** seeds.\n"
      % (LB[0.05].mean(), LB[0.10].mean(), LB[0.20].mean(), mono_lam))
    # gap to target
    w("- distance to target |worst - eps| (pooled): %.3f (0.05), %.3f (0.10), %.3f (0.20).\n"
      % (abs(W_[0.05].mean() - 0.05), abs(W_[0.10].mean() - 0.10), abs(W_[0.20].mean() - 0.20)))

    # 4. reading
    w("\n## 4. Reading\n")
    track = (W_[0.05].mean() <= W_[0.10].mean() + 1e-9) and (W_[0.10].mean() <= W_[0.20].mean() + 1e-9)
    relax = (LB[0.05].mean() >= LB[0.10].mean() - 1e-9) and (LB[0.10].mean() >= LB[0.20].mean() - 1e-9)
    w("- Knob -> level: the worst-platoon violation %s with eps and lambda %s as eps loosens "
      "-> the per-platoon CMDP exposes a tunable target (a CONSTRAINT), the positive complement "
      "to ablation #4's negative result (no fixed weight reproduces this).\n"
      % ("TRACKS (monotone)" if track else "does NOT cleanly track (see per-seed)",
         "RELAXES" if relax else "does NOT cleanly relax (see per-seed)"))
    w("- Honesty: worst is not forced to equal eps exactly; report the monotone trend + lambda "
      "relaxation, not a literal worst==eps match. Tightest eps (0.05) is the hardest to hit.\n")

    w("\n## 5. Reproduce\n```\npython results_remote/scripts/analyze_a2_eps.py\n```\n")
    w("Figure (local): a worst-platoon-vs-eps plot (per-seed lines + pooled mean, with the y=eps "
      "diagonal) + a lambda-vs-eps panel -> the clean 'knob -> level' figure.\n")

    with open(REPORT, "w", encoding="utf-8") as f:
        f.write("\n".join(O) + "\n")
    print("[a2] wrote", REPORT)
    print("[a2] worst pooled: %.3f (0.05) / %.3f (0.10) / %.3f (0.20) ; bottleneck-lam %.2f / %.2f / %.2f"
          % (W_[0.05].mean(), W_[0.10].mean(), W_[0.20].mean(),
             LB[0.05].mean(), LB[0.10].mean(), LB[0.20].mean()))


if __name__ == "__main__":
    main()
