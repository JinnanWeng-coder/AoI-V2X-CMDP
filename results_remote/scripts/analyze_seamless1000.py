"""
[RQ1-CMDP] SEAMLESS ep1000 analysis (idea 1) -- NUMBERS ONLY (operator cross-checks raw .mat).

Deploy_seamless_1200ep: train 1000 ep (memory_size=50000 UNCHANGED) then a frozen 200-ep tail
at sigma=0.3 on the SAME env (AoI not reset). Three questions:

  (1) ACCEPTANCE GATE -- the first 600 episodes must reproduce Canonical_ep600. NOTE
      AoI_evolution.mat is a rolling LAST-100-ep window (= ep900-999 for an ep1000 run), so the
      gate compares viol_rate[:, :600] / lambda[:, :600] / AoI[:, :600] (NOT AoI_evolution).

  (2) BUFFER-EVICTION hypothesis (hard) -- the ep500-600 cost-critic-loss drop was read as the
      50000-step (=500 ep) buffer first filling at ep500 and evicting the worst early transitions.
      Prediction: critic_loss_cost keeps dropping past ep600 then PLATEAUS once the buffer is fully
      turned over (~ep1000), while reward_cost (per-ep violation) stays at the dual's eps setpoint
      (must NOT fall below eps). Reported as net-mean over windows.

  (3) DEPLOYMENT -- per seed training worst (last-100ep = ep900-999) vs the frozen seamless-tail
      worst; pooled soft vs pid; compare to the ep600-seamless residual (pid 0.198 / soft 0.435).

Writes results_remote/RQ1_DEPLOY_SEAMLESS1000.md. Exit 2 if any of the 12 runs is missing.
"""
import os
import sys
import numpy as np
import scipy.io

RR = os.path.dirname(os.path.dirname(os.path.realpath(__file__)))
MODEL = os.path.join(RR, "..", "1-ModifiedMADDPGwithTDec", "model")
OUTDIR = os.path.join(MODEL, "Deploy_seamless_1200ep")
REPORT = os.path.join(RR, "RQ1_DEPLOY_SEAMLESS1000.md")
SEEDS = [2, 3, 4, 5, 6, 7]
TAU = 8.0
ARMS = [("soft", "soft", "base_seamless1200", "base_ep600"),
        ("pid",  "hard", "t8e10_pid_seamless1200", "t8e10_pid_ep600")]

_RIDX = {}
for _dp, _dns, _fn in os.walk(MODEL):
    for _dn in _dns:
        if _dn.startswith("marl_model_"):
            _RIDX.setdefault(_dn, os.path.join(_dp, _dn))
def R(name):
    return _RIDX.get(name, os.path.join(OUTDIR, name))
def load(run, fn, key):
    p = os.path.join(R(run), fn)
    return None if not os.path.exists(p) else np.asarray(scipy.io.loadmat(p)[key], np.float64)


def main():
    missing = [("marl_model_%s_seed%d_%s" % (mode, s, tag))
               for (_, mode, tag, _) in ARMS for s in SEEDS
               if load("marl_model_%s_seed%d_%s" % (mode, s, tag), "viol_rate_seamless.mat", "viol_rate_seamless") is None]
    if missing:
        sys.stderr.write("[seamless1000] missing %d runs (e.g. %s)\n" % (len(missing), missing[:4]))
        sys.exit(2)

    L = []; w = L.append
    w("# RQ1 — SEAMLESS ep1000 (train 1000 + frozen tail 200; memory_size unchanged)\n")
    w("**Auto-generated by `deploy_seamless1000_driver.ps1` -> `analyze_seamless1000.py`** "
      "(detached). NUMBERS ONLY; operator cross-checks raw `.mat`. Idea 1: does more training (with "
      "the buffer unchanged) push the cost-critic loss lower then flat, while violation stays at eps?\n")

    # ---- (1) acceptance gate: first-600 == Canonical_ep600 ----
    w("## 1. Acceptance gate — first-600 episodes reproduce Canonical_ep600\n")
    w("(AoI_evolution is a rolling last-100ep window; for ep1000 that is ep900-999, so we compare "
      "viol_rate[:, :600] / lambda[:, :600] / AoI[:, :600].)\n")
    w("| arm | seed | viol_rate[:600] | lambda[:600] | AoI[:600] |")
    w("|---|---|---|---|---|")
    gate_ok = True
    for (arm, mode, tag, ctag) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            canon = "marl_model_%s_seed%d_%s" % (mode, s, ctag)
            cells = []
            for fn, key in [("viol_rate.mat", "viol_rate"), ("lambda.mat", "lambda"), ("AoI.mat", "AoI")]:
                a = load(run, fn, key); c = load(canon, fn, key)
                if a is None or c is None:
                    cells.append("n/a")
                else:
                    ok = bool(np.allclose(a[:, :600], c[:, :600], atol=1e-4, rtol=0))
                    gate_ok = gate_ok and ok
                    cells.append("YES" if ok else "NO(%.2g)" % float(np.abs(a[:, :600] - c[:, :600]).max()))
            w("| %s | %d | %s | %s | %s |" % (arm, s, cells[0], cells[1], cells[2]))
    w("\n**GATE: %s**\n" % ("ALL PASS" if gate_ok else "FAIL — see NO cells above"))

    # ---- (2) buffer-eviction hypothesis (hard) ----
    w("## 2. Buffer-eviction hypothesis (hard, net-mean over 5 platoons)\n")
    w("| seed | critic_loss [50:150] | [400:500] | [500:600] | [900:1000] || reward_cost(viol) [400:500] | [500:600] | [900:1000] |")
    w("|---|---|---|---|---|---|---|---|---|")
    WIN = [(50, 150), (400, 500), (500, 600), (900, 1000)]
    for s in SEEDS:
        run = "marl_model_hard_seed%d_t8e10_pid_seamless1200" % s
        cl = load(run, "critic_loss_cost.mat", "critic_loss_cost").mean(axis=0)
        rc = load(run, "reward_cost.mat", "reward_cost").mean(axis=0)
        clw = ["%.3f" % cl[a:b].mean() for a, b in WIN]
        rcw = ["%.3f" % rc[a:b].mean() for a, b in WIN[1:]]
        w("| %d | %s | %s | %s | %s || %s | %s | %s |" % (s, clw[0], clw[1], clw[2], clw[3], rcw[0], rcw[1], rcw[2]))
    w("\n(prediction: critic_loss [900:1000] <= [500:600] <= [400:500] then flat; reward_cost ~ eps=0.10 throughout.)\n")

    # ---- (3) deployment ----
    w("## 3. Deployment — training worst (ep900-999) vs frozen seamless-tail worst\n")
    w("| arm | seed | train worst | tail worst | tail net-mean | gap |")
    w("|---|---|---|---|---|---|")
    pool = {"soft": [], "pid": []}
    for (arm, mode, tag, _) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            ev = load(run, "AoI_evolution.mat", "AoI_evolution")
            tw = float((ev > TAU).mean(axis=(1, 2)).max()) if ev is not None else float("nan")
            tail = load(run, "viol_rate_seamless.mat", "viol_rate_seamless").ravel()
            pool[arm].append(float(tail.max()))
            w("| %s | %d | %.3f | %.3f | %.3f | %+.3f |" % (arm, s, tw, tail.max(), tail.mean(), tail.max() - tw))
    w("")
    w("## 4. Pooled seamless-tail worst (seeds 2-7) + reference\n")
    w("| arm | ep1000 seamless tail | (ref) ep600 seamless tail |")
    w("|---|---|---|")
    ref = {"soft": "0.435+-0.175", "pid": "0.198+-0.083"}
    for arm in ("soft", "pid"):
        v = np.array(pool[arm]); w("| %s | %.3f+-%.3f | %s |" % (arm, v.mean(), v.std(ddof=1), ref[arm]))
    w("")

    # ---- sanity ----
    bad5 = []; nopkl = []
    for (_, mode, tag, _) in ARMS:
        for s in SEEDS:
            run = "marl_model_%s_seed%d_%s" % (mode, s, tag)
            v = load(run, "viol_rate_seamless.mat", "viol_rate_seamless")
            if v is None or v.reshape(-1).shape[0] != 5:
                bad5.append(run)
            if not os.path.exists(os.path.join(R(run), "Scenario_Reconstruct.pkl")):
                nopkl.append(run)
    w("## 5. Sanity\n")
    w("**(a) viol_rate_seamless has 5 entries:** %s\n" % ("ALL PASS" if not bad5 else "FAIL: " + "; ".join(bad5)))
    w("**(b) Scenario_Reconstruct.pkl present:** %s\n" % ("ALL PASS" if not nopkl else "MISSING: " + "; ".join(nopkl)))
    w("## 6. Reproduce\n```\npython results_remote/scripts/analyze_seamless1000.py\n```")

    with open(REPORT, "w", encoding="utf-8") as f:
        f.write("\n".join(L) + "\n")
    print("[seamless1000] wrote", REPORT, "| gate:", "PASS" if gate_ok else "FAIL")


if __name__ == "__main__":
    main()
